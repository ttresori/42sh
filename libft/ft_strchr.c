#include "libft.h"

char	*ft_strchr(char const *s, int c)
{
	int i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return ((char *)s + i);
		i++;
	}
	if (s[i] == '\0' && s[i] == c)
		return ((char *)s + i);
	return (NULL);
}
